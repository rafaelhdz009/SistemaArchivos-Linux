    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String archivo = this.cBoxArchivo.getSelectedItem().toString();

        if (this.txtLeer.getText().isEmpty() || this.txtEditar.getText().isEmpty()
                || this.txtExe.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (!validarNum(this.txtLeer.getText().trim())
                    && !validarNum(this.txtEditar.getText().trim())
                    && !validarNum(this.txtExe.getText().trim())) {

                JOptionPane.showMessageDialog(null, "Formato incorrecto, solo se aceptan 1 o 0", "Error", JOptionPane.ERROR_MESSAGE);

            } else {
                String mensaje = "Permisos actulizados del archivo " + archivo;
                File archTxt = new File(archivo);
                int lectura = Integer.parseInt(this.txtLeer.getText().trim());
                int escritura = Integer.parseInt(this.txtEditar.getText().trim());;
                int ejecutable = Integer.parseInt(this.txtExe.getText().trim());;

                boolean r = lectura == 1 ? archTxt.setReadable(true) : archTxt.setReadable(false);
                boolean w = escritura == 1 ? archTxt.setWritable(true) : archTxt.setWritable(false);
                boolean x = ejecutable == 1 ? archTxt.setExecutable(true) : archTxt.setExecutable(false);

                JOptionPane.showMessageDialog(null, mensaje, "Aviso", JOptionPane.INFORMATION_MESSAGE);

                cancelar();

                this.cBoxArchivo.removeAllItems();
                int rows = this.dtm.getRowCount();
                for (int i = 0; i < rows; i++) {
                    this.dtm.removeRow(0);
                }
                agregarTipo();

            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

