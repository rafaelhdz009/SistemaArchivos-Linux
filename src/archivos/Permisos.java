    private void agregarTipo() {
        try {
            cBoxArchivo.addItem("texto.txt");
            cBoxArchivo.addItem("imagen.jpg");
            cBoxArchivo.addItem("mate.odf");
            cBoxArchivo.addItem("hojaCalculo.ods");

            String txt = "texto.txt";
            String jpg = "imagen.jpg";
            String odf = "mate.odf";
            String ods = "hojaCalculo.ods";

            File archTxt = new File(txt);
            int[] permTxt = per(archTxt);

            File archJpg = new File(jpg);
            int[] permJpg = per(archJpg);

            File archOdf = new File(odf);
            int[] permOdf = per(archOdf);

            File archOds = new File(ods);
            int[] permOds = per(archOds);

            dtm.addRow(new Object[]{
                archTxt.getName(), permTxt[0], permTxt[1], permTxt[2]
            });

            dtm.addRow(new Object[]{
                archJpg.getName(), permJpg[0], permJpg[1], permJpg[2]
            });

            dtm.addRow(new Object[]{
                archOdf.getName(), permOdf[0], permOdf[1], permOdf[2]
            });

            dtm.addRow(new Object[]{
                archOds.getName(), permOds[0], permOds[1], permOds[2]
            });
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Alg√∫n archivo no se encontro, verifique", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    private int[] per(File arch) {
        int permisos[] = new int[3];

        permisos[0] = arch.canRead() ? 1 : 0;
        permisos[1] = arch.canWrite() ? 1 : 0;
        permisos[2] = arch.canExecute() ? 1 : 0;

        return permisos;
    }

